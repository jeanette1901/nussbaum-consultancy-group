// Deschide cartea
function openBook(id) {
  document.getElementById("book-overlay-" + id).style.display = "flex";
}

// Închide cartea
function closeBook(id) {
  document.getElementById("book-overlay-" + id).style.display = "none";
}

// Afișează conținutul din carte
function showContent(topicId, type) {
  if (type === "about") {
    document.getElementById("display-area-about").innerHTML =
      contentAbout[topicId];
  } else if (type === "services") {
    document.getElementById("display-area-services").innerHTML =
      contentServices[topicId];
  } else if (type === "training") {
    document.getElementById("display-area-training").innerHTML =
      contentTraining[topicId];
  }
}

// Afișează secțiunea video
function showSection(sectionId) {
  const sections = document.querySelectorAll(
    ".content-section, .content2-section",
  );

  sections.forEach((section) => {
    section.style.display = "none";
  });

  const activeSection = document.getElementById(sectionId);
  if (activeSection) {
    activeSection.style.display = "block";

    const video = activeSection.querySelector("video");
    if (video) {
      video.currentTime = 0;
      video.play();
    }
  }
}
